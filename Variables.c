#include <stdio.h>
int main()
{
    int Number = 5;
    char Star = '*';

    int age = 12;
    float price = 12.1212122f;

    printf("The given number is:- %d\n ", Number);
    printf("The given character is:- %c\n", Star);
    printf("The given age is:- %d\n", age);
    printf("The given floating price value is:- %f", price);

    return 0;
}