/*Here in this code we are going to learn taking input from the user and print that input values.*/
#include <stdio.h>
int main()
{
    int a, b;
    printf("Enter the number a:- ");
    scanf("%d", &a);
    printf("The number a is:- %d\n", a);

    printf("Enter the number b:- ");
    scanf("%d", &b);
    printf("The number b is:- %d\n", b);

    return 0;
}